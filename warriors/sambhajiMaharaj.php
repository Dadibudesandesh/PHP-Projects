            <div class="content">
                <p>
                    Sambhaji's mother was Saibai, from the family of the Nimbalkars of Phaltan. She died early (on 5 September 1659,
                    at Fort Rajgad) after a brief illness, leaving young Sambhaji in the care of his grandmother, Jijabai. Shivaji was away on various expeditions,
                    so the young Sambhaji was reared by his grandmother and his stepmothers.
                    Young Sambhaji was married to a minor, Jivubai, who was renamed Yesubai. She was the daughter of Pilajirao Shirke. It was a straightforward matrimonial alliance.
                    Pilajirao was a powerful deshmukh (in the Konkan area) in the court of Bijapur. After the marriage he provided Shivaji with access to the Konkan belt. (Incidentally,
                    Shivaji's second wife, Soyrabai (mother of Rajaram), was also from the house of the Shirkes.)
                    Sambhaji had to face the cruelties of politics at a very young age. When Mirza Raje Jaisingh cornered Shivaji, forcing on him a humiliating treaty,
                    a part of the agreement was that young Sambhaji was to be kept as collateral against Shivaji's satisfactory adherence to the agreement.
                    Sambhaji was also to serve as a courtier at the Mughal base of Aurangabad.
                    Sambhaji had been a part of Shivaji's entourage to Agra and his subsequent confinement there. When Shivaji escaped from Agra,
                    Sambhaji was left behind in the care of some friendly Brahmins at Agra and was only sent back to Maharashtra much later, once the danger to his life had faded.
                </p>
