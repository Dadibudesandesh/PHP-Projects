            <div class="title">
                <h4>The battle of succession</h4>
            </div>
            <div class="content">
                <p>
                    It is alleged that Sambhaji always had a troubled relationship with his stepmother, Soyrabai. She harboured ambitions for her son, Rajaram.
                    She wanted him to succeed Shivaji as the next Maratha king. She allegedly began to poison Shivaji's mind to that effect.
                    <br>
                    According to historians such as KhafiKhan, Mannucci, Duff, Kincaid, and Sarkar (most of whom have based their versions on the Chitnis Bakhar),
                    Sambhaji didn't help his cause either. As alleged by the Chitnis Bakhar, [2] rumours of Sambhaji's youthful indulgences were reaching Shivaji's ears.
                    There were even reports of Sambhaji's misdemeanour towards the daughter of a minister (Annaji Datto Surnis, as mentioned by historian Setu Madhavrao Pagdi.
                    Annaji Datto's daughter apparently committed suicide). Shivaji had always maintained high standards regarding the behaviour of his men towards women.
                    Obviously he didn't take kindly to this news about the crown prince. To make matters worse, Sambhaji didn't get along with many of Shivaji's ministers.
                    There were reports of Sambhaji interfering in the tax collection efforts of these ministers. Apparently Sambhaji sided with the ryot and often publicly ridiculed those ministers whom he accused of being high-handed and corrupt.
                    <br>
                    But many historians (Bendre and Shevde, the novelists Shivaji Sawant and Vishwas Patil who have researched Sambhaji extensively) have refuted allegations that cast aspersions on Sambhaji's character.
                    They have laid the blame for Sambhaji's actions squarely on a clique of ministers who resented Sambhaji and wanted Rajaram to replace him as the crown prince. According to these historians, it was calumny spread specifically to malign Sambhaji.
                    Shivaji did detain the crown prince in Fort Parnala at Parli. But what transpired afterwards has definitely left doubts about Sambhaji's judgement.
                    <br>
                    Sambhaji and his wife Yesubai apparently took off in the night and joined the Mughal forces of Diler Khan (this is disputed, as Durgabai, another of Sambhaji's wives, was said to have accompanied him,
                    while it has also been maintained that the flight to Diler Khan was part of Sambhaji's tactics to mislead the Mughal commander, Diler Khan, and blunt his attack on the Maratha kingdom in Shivaji's absence,
                    while the latter was away on his Karnataka expedition). This took place sometime in 1678. But within a year's time the crown prince realised the folly of his actions when he witnessed the atrocities of the Mughals on the ryot and escaped back to the Maratha camp at Fort Panhala (in 1680).
                    Sambhaji was apparently reconciled with his father, but was still kept under surveillance at the fort (according to Jedhe Shakavali).
                    <br>
                    Rajaram was to be married in the same year. But because of Sambhaji's disharmony with Soyrabai, Sambhaji was not invited to his own half-brother's wedding.
                    <br>
                    Then Shivaji's untimely death (on 3 April 1680) took place in Fort Raigad. This was followed by a great deal of palace intriguing. Many of Shivaji's ministers, such as Pralhad Niraji, Annaji Datto, Moropant Pingale, Balaji Avaji Chitnis, Hiroji Farzand,
                    etc, colluded with Soyrabai and installed the ten year-old Rajaram as the next king. Balaji Avaji even dispatched a letter addressed to the killedar (the fort commander) of Panhala to imprison Sambhaji with immediate effect.
                    <br>
                    Janardhan Pant Hanumante was asked to take Sambhaji into custody. But the message was discovered by some of Sambhaji's well-wishers, who promptly informed the prince of the conspiracy being hatched against his person. Sambhaji and his men immediately took charge of the fort.
                    The killedar was executed. Two hundred of the conspirators were arrested and put to death. Notable amongst the conspirators were Khandoji Naik the messenger, Bahirji Ingle, Somaji Banki, Suryaji Kank, and Hiroji Farzand (Hiroji had managed to give them the slip, but was re-arrested at Chiplun).
                    Janardhan Hanumante was seized in Kolhapur. Sambhaji then secured the support of some leading Maratha nobles such as Hambirao Mohite (the sarnobat (commander-in-chief) of the Maratha army, who was incidentally Soyrabai's brother and also the father-in-law of Rajaram - Rajaram's wife, Tarabai, was his daughter.
                    Nevertheless, he sided with Sambhaji, whom he considered the rightful heir to the throne).
                    <br>
                    Sambhaji marched along with twenty thousand troops to Fort Raigad. Yesaji Kank, an old Shivaji loyalist, opened the fort gates for the prince. Raigad fell without much resistance to Sambhaji. Even the killedar, Kanhoji Bhadwalkar, switched sides to Sambhaji.
                    <br>
                    Soyra bai was imprisoned by Sambhaji on charges of plotting against him and also poisoning the late king. Both she and her son Rajaram were imprisoned. Soyrabai died within a week, put to death on Sambhaji's orders, as alleged by a section of historians.
                    <br>
                    In February 1681, Sambhaji declared himself the new chatrapati (king) on the Maratha throne. The king wanted Ramdas Swami, the aged spiritual guide of Shivaji, to be present at the coronation ceremony. But the old man politely declined citing health reasons, and sent his emissaries instead.
                    As some historians allege, Ramdas was very much perturbed by the bloodshed that took place prior to the coronation, and it was for this reason that he declined to be personally present. But the teacher did give Sambhaji a personal letter along with his blessings.
                    In the letter Ramdas Swami advised the young king to follow in the footsteps of his great father, be more tolerant towards his people, and to avoid making decisions in anger and haste, and he advised Sambhaji to be more discreet and prudent in the future.
                    <br>
                    Heeding the saint's advice, Sambhaji decided to let bygones be bygones. He released several of the conspirators. He even reappointed Moropant Pingale as his Peshwa (the loyal Hambirao Mohite was already commander-in-chief of the armed forces).
                    <br>
                    Then it so happened that Prince Akbar (the son of Aurangzeb), who had rebelled against his father, sought refuge with Sambhaji, something which was granted (Netaji Palkar the veteran ex-senapati who had experience of the north, was sent to receive the prince).
                    <br>
                    The discredited ministers once again decided to take their chances by enlisting the help of the Mughal prince in overthrowing Sambhaji. They tried to seduce Akbar over to their side, and even plotted to poison Sambhaji.
                    <br>
                    But Prince Akbar, grateful for the protection he had received, informed Sambhaji about the attempt to be made on his life. On hearing the news, Sambhaji's fury was released once again. He arrested the conspirators (Annaji Datto, Hiroji Farzand, Balaji Avji, etc),
                    and had them trampled under the feet of an elephant. Many of Soyrabai's kinsmen were also slaughtered in what was famously called the 'Shirkan' (the genocide of the Shirkes).
                    <br>
                    Note that the name of Khando Ballal, son of Balaji Avji, figures in the material that has been written about Sambhaji, which probably indicates that Khando Ballal was pardoned on advice from Sambhaji's wife, Yesubai, and he once again resumed his position in Sambhaji's service.
                    Khando Ballal was even said to have saved Sambhaji's life in an expedition to Goa. Khando Ballal also accompanied Rajaram during his hazardous journey to Gingee, and remained his close aide in those years. Khando Ballal also served Tarabai and later Shahu,
                    and was responsible for saving Pant Pratinidhi from Shahu's wrath when Pant Pratinidhi, acting in favour of Tarabai, went against the interests of Shahu. Khando Ballal's son, Govind Chitnis, served Ramraja's cause and helped Balaji Bajirao in installing Ramraja on the Maratha throne.
                    Govind's grandson, Malhar Chitnis, wrote the famed Chitnis Bakhar. His son, Balwantrao, served Chatrapati Pratapsinh at Satara.
                    <br>
                    But this incident sowed the seeds of permanent distrust in Sambhaji's mind towards many of his men. This also led to the prominence of a Brahmin from Kannauj by the name of Kavi Kalash (Kalash the poet). Kalash soon became Sambhaji's closest confidante and adviser.
                    <br>
                    According to some historians, Kalash took advantage of Sambhaji's distrust towards his other ministers and widened the rift between them. Kavi Kalash is still a much hated figure amongst a section of Marathas and is often called 'Kalusha Kabji' (the evil instigator). Alternatively,
                    many historians swear by Kavi Kalash's devotion towards Sambhaji, making him as much a mysterious personality as his master.
                </p>
