            <div class="title">
                <h4>Sambhaji's character</h4>
            </div>
            <div class="content">
                <p>
                    Even today Sambhaji continues to evoke mixed reactions. Some hail him as the bravest Maratha warrior who ever lived while others criticise him for his indolence and a weakness for the good life. But even his critics concede his genius. They report him spending months in pleasure-seeking, only to emerge out of hibernation one fine day,
                    and put up a string of military successes, wiping out all the blots of procrastination of which he may be accused.
                    <br> Many attribute his split personality to the early plots of his step-mother and the ministers against him. This, according to these historians, made him wary about the people around him. He wasn't able to trust easily. As a result he gathered some less salubrious people around him who deliberately led him astray to suit their purposes.
                    He probably spent months under their baleful influence, only to be shaken back into reality and good sense by some of Shivaji's old loyal aides such as Hambirrao Mohite. But one has to understand that all the historians who have written adversely about Sambhaji either belonged to potential 'enemy' camps (whether Mughal, British, or Portuguese),
                    or have based their reasons on the material in the Chitnis Bakhar. After all, Sambhaji was raised by the same woman (Jijau, Shivaji's mother and Sambhaji's grandmother), who raised the great Shivaji and would have passed on the same values to Sambhaji as were passed on to his father.
                    <br> Also it must be noted that Sambhaji had to face a formidable opponent in the form of Mughal Emperor Aurangzeb himself, something which even the great Shivaji was spared. Mughal might was several times greater that that of Bijapur, and to Sambhaji's credit he put up a brave fight against the Mughals.
                    He also added several territories to Shivaji's existing kingdom including parts of the Portuguese empire in Konkan and Goa, and tracts of the Bijapur territory in Karnataka. As the acclaimed historian Narhar Kurundar says, 'if five to six forts were indeed lost to the Mughals, four to five were also wrested away from this strong enemy'.
                    <br>
                    Another charge levelled at Sambhaji is that he was hedonistic and an alcoholic, someone who was very careless and irresponsible in terms of the day-to-day administration of his kingdom. Assuming that he consumed alcohol does not automatically mean that he was negligent in his affairs.
                    Most kings of his time and those before and after him have been addicted to alcohol and opium, including his younger brother Rajaram, plus Jehangir and, for that matter, even Aurangzeb. But that need not translate into irresponsibility. His brief reign of eight years witnessed several campaigns that would not have been possible without Sambhaji's personal attention.
                </p>
            </div>
            <div class="title">
                <h4>Sambhaji's capture and execution</h4>
            </div>
            <div class="content">
                <p> At Kalash's insistence, Sambhaji decided to spend the hot summer of 1688 in a palace that had been built by Kavi Kalash at Sangameshwar, a small but climatically cool township thirty-two kilometres north of Vishalgad, and thirty-five kilometres to the north-east of Ratnagiri (this may have proved a mistake on the part of Sambhaji as it meant that he was distanced from the safe protection of the impregnable fort of Raigad).
                    <br> The news of Sambhaji's whereabouts soon reached the Mughals. The Mughal commander, Muqarrab Khan, then devised a daring plan to capture him in person. He was helped in his endeavour by the estranged brother-in-law of Sambhaji, Ganoji Shirke, who showed the Mughal army the way through the adjoining dense forests, simultaneously providing the Mughals with logistics and information about Sambhaji's movements.
                    <br> In February 1688, Sambhaji and Kavi Kalash were surrounded and captured. They were brought before Aurangzeb, who had himself encamped at Akluj (near Pandharpur).
                    <br> (Mughal historians mention that Sambhaji was in an inebriated state at the time of his capture and was surrounded by courtesans. But recent evidence states that he was accompanied not only by his wife (Yesubai) and ministers (Mhaloji Ghorpade, Dhanaji Jadhav, and Santaji Ghorpade), but also by Ranganath Swami, the leading disciple of his spiritual guru, Ramdas Swami.
                    Then the question arises of whether anyone would indulge in such indiscretions in the company of such people. Also, as far as accusations about Sambhaji's careless behaviour goes, there are letters that record Sambhaji repairing a fallen bastion at Vishalgad just two days before his capture. If Sambhaji was such an irresponsible king then would he have looked into such minute details of his kingdom? So, in fair conclusion,
                    we can only deduce that Sambhaji was betrayed and was cleverly captured by the Mughals.)
                    <br> They were both humiliated in public and paraded before the crowds in buffoons' attire. Aurangzeb then offered Sambhaji an insulting proposition. Sambhaji should surrender all his forts and the amassed Maratha treasure to the Mughals. Secondly, he should reveal the names of all Mughal officers who were secretly colluding with the Marathas, and thirdly Sambhaji should convert himself to Islam and serve the emperor for the near future.
                    In return Aurangzeb would spare Sambhaji's life.
                    </p>
                
