                <h4>Sambhaji the poet</h4>
            </div>
            <div class="content">
                <p>Sambhaji was also known for being a patron of the arts. He employed a learned man called Keshav Pandit Adhyaksh to read with him Valmiki's celebrated epic, the Ramayan.
                    As a reward, in 1684 he gave Keshav sixteen hundred small silver coins known as ladis. Moreover, the king was no mean verse writer himself.
                    He is known to have written two books of Hindi poetry. The first was Nakhshikh, in which he described the pleasures of love. The second was Nayakabhad.
                    In it he sang of the varying charms of the beauties who beguiled his leisure moments.
                </p>
