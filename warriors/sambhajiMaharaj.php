                <h4>Campaigns</h4>
            </div>
            <div class="content">
                <p>
                    Sambhaji's first campaign was in May 1680. He openly challenged the Mughal viceroy, Khan Jehan, alias Bahadur Khan Koka. The Maratha forces split into three, one attacking the Mughal territory of Surat, the second raiding Khandesh, while the third took on the imperial forces at Aurangabad.
                    The attacks proved a success for the Marathas and they came back with a rich array of booty. Sambhaji and Habirrao Mohite even looted the Mughal camp at Burhanpur, divesting it of twenty million rupees.
                    <br>
                    Sambhaji's second campaign was a daring attempt to capture the sea fort of Janjira, which unfortunately proved a failure. He even lost a brave officer by the name of Kondaji Farzand. Kondaji had managed to befriend the guarding Siddis (Abyssinians) of Janjira.
                    He had gained their trust by citing his differences with Sambhaji. He therefore managed to enter the fort and was about to capture it from within. But, unluckily, Kondaji's plan was exposed at the last minute and he was arrested and beheaded by the Siddis.
                    <br>
                    A second attempt by another officer, Dadaji Raghunath, was also foiled, this time by the cruel sea when a fierce sea wrecked his attacking boats. A third attempt was made when Siddi Misri, who was once a part of the Siddi contingent of Janjira, switched sides to join the Marathas.
                    Sambhaji made him the commander of his naval fleet. Siddi Misri led the Maratha navy against his fellow Siddis of Janjira. But Siddi Misri lost the naval battle and was himself fatally wounded.
                    <br>
                    Meanwhile, the Portuguese had aligned themselves with the Mughals. The Portuguese viceroy, Alvor, had allowed the Mughals to set up their naval base in Portuguese territory. To punish the viceroy, Sambhaji attacked Portuguese territory (1683-84), and at one point surrounded Alvor himself.
                    But as fate would have it, Aurangzeb sent a large army to take on the Marathas, forcing Sambhaji to retreat.
                    <br>
                    Sambhaji then sent a contingent to ravage the Mughal territory of Khandesh, and a second contingent to attack the Mughul prince, Shah Alam, in the south (in the Karnataka region). The Maratha forces met with success and Shah Alam's forces had to retreat on several fronts.
                    <br>
                    Then there was also a campaign against Chikkadevraja, king of Mysore. Chikkadevraja had challenged the Marathas by intruding into their territory. Sambhaji dispatched an army against him, forcing Chikkadevraja to sue for peace.
                    It is said that in order to counter the barrage of arrows launched by a huge array of archers from Mysore, Sambhaji made a battlefield preparation of oil-laden rubber armour for his soldiers which blunted their attack. At the same time he used oil-fired arrows to subdue the enemy.
                    <br>
                    In the period between 1684-1686, Aurangzeb concentrated on his southern campaign. Both Bijapur and Golkunda fell in quick succession, but Sambhaji may have made a tactical error by not aligning himself with the combined armies of Bijapur and Golkunda to face the Mughal onslaught
                    (although a group of historians maintain that Sambhaji cannot be blamed for the inactivity of the Golkunda and Bijapur monarchs. They lay the blame squarely on the monarchs of Golkunda and Bijapur for the loss of their kingdoms. They accuse the Golkunda king of being hedonistic and
                    careless and the Bijapur king of being too young and inexperienced). By 1687, the Bijapur troops who were not being utilised (mainly the Hindus amongst them, who felt ignored by the bigoted Muslim emperor) joined the Maratha ranks and captured almost one hundred and twenty towns which were once a part of Bijapur's provinces.
                    <br>
                    By now Aurangzeb's rebel son, Prince Akbar (who had sought refuge with the Marathas) was feeling the heat of his father's troops. He therefore took leave of Sambhaji and fled to Persia. But Emperor Aurangzeb was in no mood to forgive and forget.
                    <br>
                    With the Bijapur and Golkunda kingdoms annexed to the Mughal empire, Aurangzeb decided to concentrate on Sambhaji. He wanted to punish Sambhaji for harbouring his rebel son (and other rebels such as the Rajput Durgadas).
                    <br>
                    In 1688, Aurangzeb sent Sarja Khan, an ex-Bijapuri officer, to recover the lost Bijapur territory from the Marathas.
                    <br>
                    Sarja Khan was led into the jungles of Wai by Sambhaji's erstwhile commander-in-chief, Hambirao Mohite. Though Sarja Khan and his forces were routed by the Marathas, Hambirao Mohite became a casualty of that battle. The death of his most trusted noble was a great blow for Sambhaji.
                </p>
