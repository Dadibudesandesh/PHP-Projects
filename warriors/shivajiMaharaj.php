            <div class="title">
                <h4>Coronation and Conquests</h4>
            </div>
            <div class="content">
                <p>
                    After having consolidated a considerable control over territories adjoining Poona and Konkan, Shivaji decided to adopt a Kingly title and establish the first Hindu Sovereignty in South, that was till now dominated by Muslims. He was crowned the King of Marathas on June 6, 1674, at Raigadh in an elaborate coronation ceremony. 
                    The Coronation was officiated by Pandit Gaga Bhatt in front of a gathering of around 50,000 people. He took upon several titles like Chhtrapati (paramount sovereign), Shakakarta (founder of an era), Kshatriya Kulavantas (head of Kshatriyas) and Haindava Dharmodhhaarak (one who uplifts the sanctity of Hinduism).
                    Post-coronation, the Marathas under Shivaji’s directives launched aggressive conquest efforts to consolidate most of the Deccan states under the Hindu Sovereignty. He conquered Khandesh, Bijapur, Karwar, Kolkapur, Janjira, Ramnagar and Belgaum. He captured forts at Vellore and Gingee, controlled by the Adil Shahi rulers. 
                    He also came to an understanding with his step-brother Venkoji over his holdings over Tanjavur and Mysore. What he aimed at was to unify the Deccan states under the rule of a native Hindu ruler and protect it from outsiders like the Muslims and Mughals.
                </p>
