                <h4>Administration</h4>
            </div>
            <div class="content">
                <p>
                    Under his reign, the Maratha administration was established where Chhatrapati was the supreme sovereign and a team of eight ministers were appointed to oversee the proper enforcement of various policies. These eight ministers reported directly to Shivaji and were given a lot of power in terms of execution of policies formulated by the King. 
                    These eight ministers were - <br><br>
                
                    (1) The Peshwa or Prime Minister, who was head of general
                        administration and represented the king in his absence.
                        <br>
                    (2) The Majumder or the Auditor was responsible for maintain 
                        the financial health of the kingdom
                        <br>
                    (3) The PanditRao or Chief Spiritual Head was responsible for 
                         overseeing the spiritual well-being of the kindom, fix dates 
                        for religious ceremonies and oversee charitable programs 
                        undertaken by the king.
                        <br>
                    (4) The Dabir or Foreign Secretary was entrusted with the
                        responsibility of advising the king on matters of 
                        foreign policies.
                        <br>
                    (5) The Senapati or Military General was in charge of overseeing
                        every aspect of the military including organization, recruitment
                        and training of soldiers. He also was the strategic advisor 
                        of the king in the time of a war.
                        <br>
                    (6) The Nyayadhish or Chief Justice saw formulations of law and their
                        subsequent enforcement, civil, judicial as well as military.
                        <br>
                    (7) The Mantri or Chronicler was responsible for keeping elaborate 
                         records of everything the king did in his daily life. 
                         <br>
                    (8) The Sachiv or Superintendant was in charge of royal correspondence.
            
                </p>
