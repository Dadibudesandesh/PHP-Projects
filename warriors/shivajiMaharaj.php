                <h4>Conflicts with the Mughals</h4>
            </div>
            <div class="content">
                <p>
                    Shivaji’s conflicts with the Bijapuri Sultanate and his continuous victories brought him under the radar of Mughal Emperor Aurangzeb. Aurangzeb saw him as a threat to expansion of his imperial intent and concentrated his efforts on eradicating the Maratha threat. 
                    Confrontations began in 1957, when Shivaji’s generals raided and looted Mughal territories near Ahmednagar and Junnar. However, Aurangzeb’s retaliation was thwarted by arrival of rainy season and battle for succession back in Delhi. Aurangzeb directed Shaista Khan, 
                    Governor of Deccan and his maternal uncle, to subdue Shivaji.Shaista Khan launched a massive attack against Shivaji, capturing several forts under his control and even his capital Poona. Shivaji retaliated back by launching a stealth attack on Shaista Khan, 
                    eventually injuring him and evicting him from Poona. Shaista Khan later arranged multiple attacks on Shivaji, severely reducing his holds of forts in the Konkan region. To replenish his depleted treasury, Shivaji attacked Surat, an important Mughal trading center and looted the Mughal wealth. 
                    An infuriated Aurangzeb sent his chief general Jai Singh I with an army of 150,000. The Mughal forces made considerable dent, sieging forts under Shivaji’s control, extracting money and slaughtering soldiers in their wake. Shivaji agreed to come to an agreement with Aurangzeb to prevent further 
                    loss of life and the Treaty of Purandar was signed between Shivaji and Jai Singh on June 11, 1665. Shivaji agreed to surrender 23 forts and pay a sum of 400000 as compensation to the Mughal Empire. Aurangzeb invited Shivaji to Agra with an aim to use his military prowess to consolidate Mughal empires in Afghanistan. 
                    Shivaji travelled to Agra with his eight year old son Sambhaji and was offended by Aurangzeb’s treatment of him. He stormed out of the court and an offended Aurangzeb placed him under house arrest. But Shivaji once again used his wit and cunning to escape the imprisonment. 
                    He feigned severe illness and arranged for baskets of sweets to be sent to temple as offerings for prayer. He disguised as one of the carriers and hid his son in one of the baskets, and escaped on August 17, 1666. In subsequent times, Mughal and Maratha hostilities were pacified to a large extent by constant mediation through Mughal Sardar Jaswant Singh. 
                    Peace lasted till 1670, after which Shivaji launched an all-out offense against the Mughals. He recovered most of his territories sieged by the Mughals within four months.
                
                </p>
