                <h4>Struggles with Bijapur</h4>
            </div>
            <div class="content">
                <p>By 1645, Shivaji acquired control of several strategic from under the Bijapur Sultanate around Pune – Torna from Inayat Khan, Chakan from FirangojiNarsala, Kondana from Adil Shahi Governor, 
                    along with Singhagarh and Purandar. Following his success, he had emerged as a threat for Mohammed Adil Shah who gave the order to imprison Shahaji in 1648. 
                    Shahaji was released on condition that Shivaji kept a low profile and kept from further conquests. Shivaji resumed his conquests after Shahaji’s death in 1665 by acquiring the valley of Javali from Chandrarao More, 
                    a Bijapuri jaigirdaar. Mohammed Adil Shah sent Afzal Khan, a powerful general in his employ to subdue Shivaji.
                    The two met in a private rendezvous on November 10, 1659 to discuss terms of negotiation. Shivaji anticipated it to be a trap and he arrived prepared wearing armor and concealing a metal tiger claw.  
                    When Afzal Khan attacked Shivaji with a dagger, he was saved by his armour and Shivaji retaliated by attacking Afzal Khan with the tiger’s claw, mortally injuring him. He ordered his forces to launch an assault on the leaderless Bijapuri contingents. 
                    Victory was easy for Shivaji in the Battle of Pratapgarh, where around 3000 Bijapuri soldiers were killed by the Maratha forces. Mohammed Adil Shah next sent a larger army under the command of General Rustam Zaman who faced Shivaji in the Battle of Kolhapur. 
                    Shivaji secured victory in a strategic battle causing the general to flee for his life. Mohammed Adil Shah finally saw victory when his general Siddi Jauhar successfully sieged the fort of Panhala on September 22, 1660. Shivaji recaptured the Fort of Panhal later in 1673.
                </p>
